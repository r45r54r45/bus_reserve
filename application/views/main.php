function findPos(arr,data){
	for (var i = 0; i < arr.length; i++) {
		if(arr[i]>data){
			return arr[i];
		}
	}
	return false;
}
function m_bus(){
	setInterval(function(){
		var now=new Date().format("HHmm");
		var time=findPos(timeadd(mbus(),20),parseInt(now));
			if(time.toString().length==3)time="0"+time;
		var diff=timediff(time,now);
		$("#mbus").text(time_format(time)+" ("+time_format(diff)+" 남음)");
	},1000);
}

function songdo_shuttle(){
	setInterval(function(){
		var now=new Date().format("HHmm");
		$("#h_s").text(time_format(findPos(h_time_s,parseInt(now))));
		$("#h_w").text(time_format(findPos(h_time_w,parseInt(now))));
		$("#d_s").text(time_format(findPos(d_time_s,parseInt(now))));
		$("#d_w").text(time_format(findPos(d_time_w,parseInt(now))));
	},1000);
}
function time_format(data){
	var h;
	var m;
	var k=data.toString();
	if(data==false)return "정보 없음";
	if(k.length==3){
		h=k.substring(0,1);
	 m=k.substring(1,3);
	}else{
	h=k.substring(0,2);
 m=k.substring(2,4);
 }
	if(h=="00")return m+"분";
	return h+"시 "+m+"분";
}
